package com.ssm.pt.builder.exception;

/**
 * @author zengsl
 *
 */
public class DynamicStatementException extends RuntimeException {

	public DynamicStatementException() {
		super();
	}

	public DynamicStatementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DynamicStatementException(String arg0) {
		super(arg0);
	}

	public DynamicStatementException(Throwable arg0) {
		super(arg0);
	}

}
